package DTO;

public class MasterAddressDTO {
	private String zip,kenOrCapital,cityName,ward,townNum;

	public String getZip() {
		return zip;
	}

	public void setZip(String zip) {
		this.zip = zip;
	}

	public String getKenOrCapital() {
		return kenOrCapital;
	}

	public void setKenOrCapital(String kenOrCapital) {
		this.kenOrCapital = kenOrCapital;
	}

	public String getCityName() {
		return cityName;
	}

	public void setCityName(String cityName) {
		this.cityName = cityName;
	}

	public String getWard() {
		return ward;
	}

	public void setWard(String ward) {
		this.ward = ward;
	}

	public String getTownNum() {
		return townNum;
	}

	public void setTownNum(String townNum) {
		this.townNum = townNum;
	}
}
